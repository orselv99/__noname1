//! ==========================================================================
//! commands 모듈 - Tauri 커맨드 (프론트엔드 ↔ Rust 백엔드 인터페이스)
//! ==========================================================================
//!
//! C++ 개발자를 위한 설명:
//! - Tauri 커맨드: 프론트엔드(JS/TS)에서 호출 가능한 Rust 함수
//! - C++ 비교: Qt의 Q_INVOKABLE 메서드 또는 Node.js Native Addon 바인딩
//!
//! 호출 예시 (프론트엔드):
//! ```typescript
//! import { invoke } from '@tauri-apps/api/core';
//! const result = await invoke('login', { email: '...', password: '...' });
//! ```
//!
//! 모듈 구조:
//! - `ai`: AI 분석 (요약, 태그, 임베딩)
//! - `auth`: 인증 (로그인, 로그아웃, 비밀번호)
//! - `documents`: 문서 CRUD
//! - `media`: 미디어 처리 (이미지 다운로드)
//! - `rag`: RAG 챗봇 및 검색
//! ==========================================================================

